uint8_t HeadCompare(uint8_t data)
{
	if (data == head_bytes[index])
		{
		if (index == 1)
			{
			index = 0;
			return 1;
			}
		else{
			index++;
			return 0;
		}
	}
		return 1;
}

uint8_t DataStorage(uint8_t data){
	if (index_2 ==24){
		index_2 = 0;
		return 2;
		}
	version_bytes[index_2] = data;
	index_2++;
	return 1;
}

void UART_Compare(uint8_t data)
{
	static int current_step = 0;
	switch(state)
	{
		case 0:
			state = HeadCompare(data);
			current_step = 0;
		case 1:
			if (current_step >=6 && current_step <=30){
				state = DataStorage(data);
				current_step++;
				}
			else{
				current_step = 0;
	}
}
}

